export function getLocation(id: string): LocationDef | undefined {
  return LOCATIONS[id];
}

export function getAllLocationIds(): string[] {
  return Object.keys(LOCATIONS);
}

/** 时段类型 */
export type TimeOfDay = 'morning' | 'afternoon' | 'evening' | 'night';

/**
 * 获取地点的动态描述
 * 优先级：天气 > 时段 > 季节 > 基础描述
 */
export function getLocationDescription(
  locationId: string,
  timeOfDay: TimeOfDay,
  weather: string,
  season: string
): string {
  const location = LOCATIONS[locationId];
  if (!location) return '未知地点';

  const { descriptions, description } = location;

  // 优先级1：天气描述
  if (weather === '雨' && descriptions.rainy) {
    return descriptions.rainy;
  }
  if (weather === '雪' && descriptions.snowy) {
    return descriptions.snowy;
  }

  // 优先级2：时段描述
  if (descriptions[timeOfDay]) {
    return descriptions[timeOfDay]!;
  }

  // 优先级3：季节描述
  const seasonKey = season.toLowerCase() as 'spring' | 'summer' | 'autumn' | 'winter';
  if (descriptions[seasonKey]) {
    return descriptions[seasonKey]!;
  }

  // 默认返回基础描述
  return description;
}
