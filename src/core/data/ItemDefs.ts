/**
 * 根据ID获取物品定义
 */
export function getItem(id: string): ItemDef | undefined {
  return ITEMS[id];
}

/**
 * 获取所有物品ID列表
 */
export function getAllItemIds(): string[] {
  return Object.keys(ITEMS);
}

/**
 * 根据类别获取物品列表
 */
export function getItemsByCategory(category: ItemCategory): ItemDef[] {
  return Object.values(ITEMS).filter(item => item.category === category);
}

/**
 * 根据稀有度获取物品列表
 */
export function getItemsByRarity(rarity: ItemRarity): ItemDef[] {
  return Object.values(ITEMS).filter(item => item.rarity === rarity);
}
