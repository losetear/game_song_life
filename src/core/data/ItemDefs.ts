/**
 * 物品定义系统
 * 定义游戏中所有可交互的物品类型及其属性
 */

// 物品大类
export type ItemCategory = 'food' | 'drink' | 'medicine' | 'tool' | 'clothing' | 'material' | 'luxury' | 'book' | 'weapon' | 'gift';

// 物品稀有度
export type ItemRarity = 'common' | 'uncommon' | 'rare' | 'legendary';

export interface ItemDef {
  id: string;            // 唯一ID，如 "mantou", "herb_medicine"
  name: string;          // 显示名，如 "馒头", "草药"
  category: ItemCategory;
  rarity: ItemRarity;
  description: string;   // 一句话描述
  basePrice: number;     // 基础价格（文钱）
  effects: {
    hunger?: number;     // 饱食度变化
    fatigue?: number;    // 精力变化
    health?: number;     // 健康变化
    mood?: number;       // 心情变化
  };
  stackable: boolean;    // 是否可堆叠
  maxStack: number;      // 最大堆叠数
  usable: boolean;       // 是否可以直接使用
  useNarrative?: string; // 使用时的叙事文本模板
  icon: string;          // emoji图标
}

